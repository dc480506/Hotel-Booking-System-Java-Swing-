    private void avlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_avlActionPerformed
        // TODO add your handling code here:
        try{
            cb.setSelected(false);
            name=user_text.getText();
            nav.setText("*Username not available.Try something else");
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/hotel_booking","root","dev12345");
            PreparedStatement stmt=con.prepareStatement("SELECT username FROM login_details WHERE username='"+name+"'");
            ResultSet rs=stmt.executeQuery();
            if(name.equals("")){
                nav.setText("*Username cannot be empty");
                nav.setVisible(true);
            }else if(!rs.absolute(1)){
                cb.setVisible(true);
                cb.setSelected(true);
                nav.setVisible(false);
            }else{
                cb.setVisible(false);
                nav.setVisible(true);
            }
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_avlActionPerformed

