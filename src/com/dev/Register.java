    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // TODO add your handling code here:
        if((pass1.getText()).equals(pass2.getText())){
            try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/hotel_booking","root","dev12345");
            Statement stmt=(Statement)con.createStatement();
            String pass=pass1.getText();
            String custom=customer.getText();
            String mob=mobno.getText();
            String mail=email.getText();
            stmt.executeUpdate("INSERT INTO `login_details`(`username`, `password`,`name`,`mobno`,`email`) VALUES ('"+name+"','"+pass+"','"+custom+"','"+mob+"','"+mail+"');");
            new Redirect();
            dispose();
            }catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(null,e.getMessage(),"Error",1);
        }
        }else{
           pm.setVisible(true);
        }
    }//GEN-LAST:event_registerActionPerformed

